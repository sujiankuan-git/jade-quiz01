db.serialize(() => {
    db.all(`SELECT id FROM questions WHERE type = 'image' ORDER BY id`, [], (err, rows) => {
        if (err) return console.error(err);
        
        const stmt = db.prepare(`UPDATE questions SET explanation = ? WHERE id = ?`);

        let updated = 0;
        rows.forEach((row, index) => {
            if (index < expls.length) {
                stmt.run([expls[index], row.id]);
                updated++;
            }
        });
        
        stmt.finalize();
        console.log(`Successfully updated explanations for ${updated} image questions.`);
    });
});
